// Languages map (language name -> array of country IDs)
export const LANGUAGES_MAP = {};
Object.entries(COUNTRY_INFO).forEach(([id, info]) => {
  if (!COUNTRIES[id]) return;
  info[2].split(" / ").map(l => l.trim()).forEach(lang => {
    if (!LANGUAGES_MAP[lang]) LANGUAGES_MAP[lang] = [];
    LANGUAGES_MAP[lang].push(id);
  });
});
